 public class ArrayExample {
    public static void main(String[] args) {
        // int एरे को इनिशियलाइज़ करना
        int arr[] = {1, 2, 3};

        // एरे के तत्वों को प्रिंट करना
        for (int i = 0; i < 3; i++) {
            System.out.println(arr[i]); // या sout(arr[i]);
        }
    }
}
